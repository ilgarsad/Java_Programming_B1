package day16_loops;

public class C3_HelloWorld {
    public static void main(String[] args) {

        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("-----------");

        int num = 1;

        while (num <= 5) {
            System.out.println(num + ") Hello World");
            num++;
        }

    }
}
