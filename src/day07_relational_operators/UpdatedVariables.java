package day07_relational_operators;

public class UpdatedVariables {

    public static void main(String[] args) {

        int a = 10; //declared and assigned value
            a = 11; //reassigned
            a++;    //reassigned        ---> 12

        System.out.println(a);

            ++a;    //reassigned        ---> 13

        System.out.println(a);
        System.out.println();

        int x = 10;
        x = x + 4;          // 14

        x += 4;

        System.out.println(x);      // 18


    }
}
